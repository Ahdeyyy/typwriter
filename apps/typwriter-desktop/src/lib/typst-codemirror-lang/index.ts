// ─── Fold support ─────────────────────────────────────────────────────────────
//
// Folds for all block-like structures.

const typstFolding = foldNodeProp.add({
    // Code blocks and content blocks
    CodeBlock(node) {
        return foldInside(node);
    },
    ContentBlock(node) {
        return foldInside(node);
    },
    // Parenthesized expressions (arrays, dicts, args, params)
    Parens(node) {
        return foldInside(node);
    },
    // Block comments
    BlockComment(node) {
        return { from: node.from + 2, to: node.to - 2 };
    },
    // Equations (multiline)
    Equation(node) {
        return foldInside(node);
    },
});

// ─── Auto-indent support ──────────────────────────────────────────────────────

const typstIndent = indentNodeProp.add({
    CodeBlock(context) {
        return context.baseIndent + context.unit;
    },
    ContentBlock(context) {
        return context.baseIndent + context.unit;
    },
    Parens(context) {
        return context.baseIndent + context.unit;
    },
});

// ─── Language definition ─────────────────────────────────────────────────────

export const typstLanguage = LRLanguage.define({
    name: "typst",
    parser: typstParser.configure({
        props: [
            typstHighlighting,
            typstFolding,
            typstIndent,
        ],
    }),
    languageData: {
        commentTokens: { line: "//", block: { open: "/*", close: "*/" } },
        // Auto-close pairs
        closeBrackets: {
            brackets: ["(", "[", "{", '"', "$"],
            // Content block
            before: ")]}$",
        },
        indentOnInput: /^\s*[}\])]$/,
    },
});

