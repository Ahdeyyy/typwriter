/**
 * Provides context-aware autocomplete for Typst source files.
 * - Keywords and built-ins anywhere
 * - Math symbols inside equations
 */
function typstCompletions(context: CompletionContext) {
    // Check if we're inside an Equation node for math-specific completions
    const nodeBefore = context.state.doc.sliceString(
        Math.max(0, context.pos - 50),
        context.pos
    );

    // Simple heuristic: are we inside a $ ... $ block?
    const dollarCount = (nodeBefore.match(/\$/g) || []).length;
    const inMath = dollarCount % 2 === 1;

    const word = context.matchBefore(/[\w\-]+/);
    if (!word) return null;
    if (word.from === word.to && !context.explicit) return null;

    const options = inMath
        ? [...mathSymbolCompletions, ...keywordCompletions]
        : [...keywordCompletions, ...builtinFunctionCompletions];

    return {
        from: word.from,
        options,
    };
}

// ─── Public API ───────────────────────────────────────────────────────────────

/**
 * Returns a LanguageSupport instance for Typst.
 *
 * @example
 * import { typst } from "@codemirror/lang-typst";
 * new EditorView({ extensions: [typst()] })
 */
export function typst() {
    return new LanguageSupport(typstLanguage, [
        typstLanguage.data.of({ autocomplete: typstCompletions }),
    ]);
}
